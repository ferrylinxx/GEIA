type WidenTranslationValues<T> = {
  [K in keyof T]:
    T[K] extends string
      ? string
      : T[K] extends readonly string[]
        ? readonly string[]
        : T[K] extends readonly (infer U)[]
          ? readonly WidenTranslationValues<U>[]
          : T[K] extends object
            ? WidenTranslationValues<T[K]>
            : T[K]
}

